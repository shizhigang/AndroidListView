package com.sw.androidlistview.entity;

public class Group {
	 private int id;
	 private String title;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	@Override
	public String toString() {
		return "Group [id=" + id + ", title=" + title + "]";
	}
	 
}
